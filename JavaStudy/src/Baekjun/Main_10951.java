package Baekjun;

import java.util.Scanner;

public class Main_10951 {
	public static void main(String[] args) {
		Scanner s = new Scanner(System.in);

		int n1 = 0, n2 = 0;
	
		while (s.hasNext()) {
			n1 = s.nextInt();
			n2 = s.nextInt();
			System.out.println(n1 + n2);
		}
	
	}
}
